create table airport
	(acode CHAR(3) PRIMARY KEY,
	wifi CHAR(20),
	city VARCHAR2(50) NOT NULL);
 
grant select on airport to public;
 
create table flightschedule
	(
departureDate date,  
departureTime char(4),
arrivalDate date,
arrivalTime char(4),
PRIMARY KEY (departureDate, departureTime, arrivalDate, arrivalTime));
	
grant select on flightschedule to public;

CREATE TABLE PlaneType (
	ptName VARCHAR2(30) PRIMARY KEY,
	capacity INTEGER,
	wifi CHAR(20),
	entertainment VARCHAR2(20));
grant select on PlaneType to public;
 
CREATE TABLE Plane (
	pNo VARCHAR2(3) PRIMARY KEY,
	ptName VARCHAR2(30) NOT NULL,
	FOREIGN KEY (ptName) REFERENCES PlaneType
		ON DELETE CASCADE);
grant select on Plane to public;
 
create table flight
	(
	flightNo CHAR(8) PRIMARY KEY,
	airline VARCHAR2(20) NOT NULL,
	service VARCHAR2(15),
	fCost INTEGER,
departureDate DATE,
departureTime char(4),
arrivalDate DATE,
arrivalTime char(4),
pNo VARCHAR(3),
FOREIGN KEY (departureDate, departureTime, arrivalDate, arrivalTime) REFERENCES FlightSchedule,
FOREIGN KEY (pNo) REFERENCES plane);
 
grant select on flight to public;

CREATE TABLE Passenger (
	passportNo CHAR(8) PRIMARY KEY,
	fName VARCHAR2(20) NOT NULL,
	lName VARCHAR2(20) NOT NULL);
 
grant select on Passenger to public;

CREATE TABLE Member (
	userID VARCHAR2(50) PRIMARY KEY,
	email VARCHAR2(51) UNIQUE,
passportNo CHAR(8) NOT NULL,
	points INTEGER,
	FOREIGN KEY (passportNo) REFERENCES Passenger);
 
grant select on Member to public;

CREATE TABLE Ticket (
	tID VARCHAR2(8) PRIMARY KEY,
	passportNo CHAR(8) NOT NULL,
	flightNo CHAR(8) NOT NULL,
	price INTEGER,
	FOREIGN KEY (passportNo) REFERENCES Passenger,
	FOREIGN KEY (flightNo) REFERENCES Flight);
 
grant select on ticket to public;

create table airportsused
	 (
	origin_aCode CHAR(3),
	departureDate DATE,
	departureTime char(4),
destination_aCode CHAR(3),
	arrivalDate DATE,
	arrivalTime char(4),
PRIMARY KEY (origin_aCode, departureDate, departureTime, destination_aCode, arrivalDate, arrivalTime),
FOREIGN KEY (origin_aCode) REFERENCES Airport (aCode),
FOREIGN KEY (destination_aCode) REFERENCES Airport (aCode),
FOREIGN KEY (departureDate, departureTime, arrivalDate, arrivalTime) REFERENCES FlightSchedule);
grant select on airportsused to public;

CREATE TABLE SeatType (
	stName VARCHAR2(20) PRIMARY KEY,
	availability INTEGER,
	legRoom CHAR(5),
	reclining CHAR(5),
	stcost INTEGER);
	
grant select on SeatType to public;
 
CREATE TABLE Seat (
seatNo CHAR(3),
stName VARCHAR2(15) NOT NULL,
flightNo CHAR(8) NOT NULL,
available CHAR(5),
sCost INTEGER,
PRIMARY KEY (seatNo, flightNo, stName),
FOREIGN KEY (stName) REFERENCES SeatType,
FOREIGN KEY (flightNo) REFERENCES Flight);
grant select on seat to public;

CREATE TABLE PlaneSeat (
ptName VARCHAR(30),
stName VARCHAR(20),
numSeats INTEGER,
PRIMARY KEY (ptName, stName),
FOREIGN KEY (ptName) REFERENCES PlaneType,
FOREIGN KEY (stName) REFERENCES SeatType
);
grant select on PlaneSeat to public;

